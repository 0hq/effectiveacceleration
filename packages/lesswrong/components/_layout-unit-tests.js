import React from 'react';
import { render, shallow, mount, configure } from 'enzyme';
import { assert, should, expect } from 'meteor/practicalmeteor:chai';
import { Components } from 'meteor/vulcan:core';
import Adapter from 'enzyme-adapter-react-16';
configure({ adapter: new Adapter() })

import { sinon } from 'meteor/practicalmeteor:sinon';

describe('Layout', () => {
  it('does NOT render Intercom if currentUser.hideIntercom is true', () => {
    const layout = shallow(<Components.Layout currentUser={{hideIntercom:true}} />)
    expect(layout.find("#intercome-outer-frame")).to.have.length(0);
  });
});
